#!/usr/bin/env node
/**
 * Generate high-quality font glyph data from Poppins Light.
 * Each glyph is rendered at high resolution, then we measure its actual width
 * to enable proportional spacing. Output: src/fontAtlasData.ts
 */

const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

const FONT_PATH = '/tmp/Poppins-Light.ttf';
const CHARS = ' ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789-+.,:/\'()';

const CELL_H = 36;       // glyph cell height
const MAX_CELL_W = 28;   // max glyph cell width (for wide chars like M, W)
const FONT_SIZE = 28;

const SRC_DIR = path.join(__dirname, '..', 'src');
const tmpDir = path.join(__dirname, 'tmp-glyphs');
if (!fs.existsSync(tmpDir)) fs.mkdirSync(tmpDir, { recursive: true });

console.log(`Generating Poppins Light font data for ${CHARS.length} characters...`);

const glyphEntries = {};

for (let i = 0; i < CHARS.length; i++) {
  const ch = CHARS[i];
  const tmpFile = path.join(tmpDir, `g${i}.gray`);

  // Escape special chars for shell
  let label = ch;
  if (ch === "'") label = "\\'";
  if (ch === '(') label = '\\(';
  if (ch === ')') label = '\\)';

  try {
    // Render white text on black at full cell size
    execSync(
      `magick -size ${MAX_CELL_W}x${CELL_H} xc:black ` +
      `-font "${FONT_PATH}" -pointsize ${FONT_SIZE} -fill white -gravity center ` +
      `-annotate +0+1 "${label}" ` +
      `-blur 0x0.6 ` +
      `-depth 8 gray:${tmpFile}`,
      { stdio: 'pipe' }
    );
  } catch (e) {
    const empty = Buffer.alloc(MAX_CELL_W * CELL_H, 0);
    fs.writeFileSync(tmpFile, empty);
  }

  // Read raw grayscale pixels
  const pixels = new Uint8Array(fs.readFileSync(tmpFile));

  // Measure actual glyph width by finding leftmost and rightmost non-zero columns
  let minCol = MAX_CELL_W;
  let maxCol = 0;
  for (let row = 0; row < CELL_H; row++) {
    for (let col = 0; col < MAX_CELL_W; col++) {
      if (pixels[row * MAX_CELL_W + col] > 10) {
        if (col < minCol) minCol = col;
        if (col > maxCol) maxCol = col;
      }
    }
  }

  let trimmedW, trimmedPixels;
  if (ch === ' ') {
    // Space: fixed width, no pixels
    trimmedW = 8;
    trimmedPixels = Buffer.alloc(trimmedW * CELL_H, 0);
  } else if (minCol > maxCol) {
    // Empty glyph
    trimmedW = 6;
    trimmedPixels = Buffer.alloc(trimmedW * CELL_H, 0);
  } else {
    // Add 1px padding on each side
    const pad = 1;
    const cropLeft = Math.max(0, minCol - pad);
    const cropRight = Math.min(MAX_CELL_W - 1, maxCol + pad);
    trimmedW = cropRight - cropLeft + 1;

    trimmedPixels = Buffer.alloc(trimmedW * CELL_H);
    for (let row = 0; row < CELL_H; row++) {
      for (let col = 0; col < trimmedW; col++) {
        trimmedPixels[row * trimmedW + col] = pixels[row * MAX_CELL_W + (cropLeft + col)];
      }
    }
  }

  glyphEntries[ch] = {
    w: trimmedW,
    data: trimmedPixels.toString('base64'),
  };

  if (i % 10 === 0) process.stdout.write('.');
}

console.log('\nWriting TypeScript...');

let ts = `// Auto-generated Poppins Light font data — do not edit manually
// Generated by scripts/generate-font-atlas.js
// Each glyph is variable-width x ${CELL_H}px, grayscale, base64-encoded

export const GLYPH_H = ${CELL_H};

// Per-character data: w = pixel width, d = base64 grayscale pixels
export const GLYPH_DATA: Record<string, { w: number; d: string }> = {\n`;

for (const [ch, entry] of Object.entries(glyphEntries)) {
  const key = ch === "'" ? "\"'\"" : ch === '\\' ? '"\\\\"' : JSON.stringify(ch);
  ts += `  ${key}: { w: ${entry.w}, d: "${entry.data}" },\n`;
}

ts += `};\n\n`;

ts += `/** Decode base64 glyph to Uint8Array */
export function decodeGlyph(b64: string): Uint8Array {
  const bin = atob(b64);
  const arr = new Uint8Array(bin.length);
  for (let i = 0; i < bin.length; i++) arr[i] = bin.charCodeAt(i);
  return arr;
}
`;

fs.writeFileSync(path.join(SRC_DIR, 'fontAtlasData.ts'), ts);
console.log('Saved src/fontAtlasData.ts');

// Cleanup
execSync(`rm -rf ${tmpDir}`);
console.log('Done!');
